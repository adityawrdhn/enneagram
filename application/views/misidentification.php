    Apa yang sama: Pengamat (5) dan Skeptis Setia (6) keduanya jenis pusat kepala dan sayap masing-masing. Keduanya bisa analitis, reflektif, bijaksana,
    ditarik dan ragu-ragu untuk mengambil tindakan (terutama lebih fobia Six).
    <br/>
    <br/>
    Apa yang berbeda: Pengamat melepaskan diri dari atau meredam perasaan mereka, milah situasi dan biasanya menunda respon mereka. Skeptis Loyal, di sisi
    lain, bereaksi segera untuk keadaan dan memperbesar bahaya dirasakan, sering intens dan ketakutan.
    <br/>
    <br/>
    <strong>Jenis 5 dan 7</strong>
    <br/>
    <br/>
    Apa yang sama: Pengamat (5) dan Epicures (7) adalah jenis pusat kepala. Selain itu, Observer adalah titik keamanan Epicure itu, dan Epicure adalah titik
    stres Observer. Kedua jenis cinta ide dan mandiri, berpengetahuan dan inventif. Mereka juga menghindari perasaan yang menyakitkan. Pengamat stres menjadi
    lebih externalized, sosial, aktif dan berorientasi pada kemungkinan. Epicures dalam keamanan menjadi lebih terinternalisasi, soliter, jeli dan berorientasi
    dalam hati.
    <br/>
    <br/>
    Apa yang berbeda: Pengamat menghindari perasaan yang kuat, mengandung kebutuhan mereka dan keinginan, menyederhanakan hidup dan menarik untuk melindungi
    batas-batas mereka. Sebaliknya, Epicures aktif mencari positif, mengungkapkan kebutuhan mereka dan keinginan, menjadi luas dan padat, dan sepakan batas dan
    batas.
    <br/>
    <br/>
    <strong>Jenis 5 dan 8</strong>
    <br/>
    <br/>
    Apa yang serupa: The Observer (5) adalah (8) titik stres Protector ini, dan Protector adalah titik keamanan Observer. Kedua jenis penasaran, nilai hormat
    dan kebenaran, menolak kontrol, dan menjadi posesif ruang dan sumber daya kunci. Pengamat keamanan menjadi lebih terlibat, keluar dan ekspresif keinginan
    mereka, perasaan dan kemarahan. Pelindung di stres menjadi lebih ditarik, terkendali dan reflektif.
    <br/>
    <br/>
    Apa yang berbeda: Secara umum, pengamat adalah yang ditarik, berisi dan diukur dari sembilan jenis Enneagram. Mereka cenderung untuk menghemat energi,
    mengurangi kebutuhan mereka dan berpikir sebelum bertindak. Pelindung, sebaliknya, adalah yang paling luas, ekspresif dan berlebihan dari sembilan jenis
    Enneagram. Mereka memperluas energi, langsung mengungkapkan keinginan dan kemarahan mereka, dan sering bertindak sebelum berpikir.
    <br/>
    <br/>
    <strong>
        Jenis 5 dan 9
        <br/>
    </strong>
    <br/>
    Apa yang sama: Pengamat (5) dan mediator (9) dianggap jenis mirip karena keduanya dapat ditarik, introvert, bijaksana dan tidak mengganggu. Mereka
    cenderung untuk menarik atau menarik kembali dari yang terlalu dipengaruhi oleh lingkungan mereka.
    <br/>
    <br/>
    Apa yang berbeda: Pengamat biasa melepaskan diri dari orang lain dan menegaskan batas-batas mereka dalam perlindungan diri, sedangkan mediator adalah yang
    paling mampu melepaskan diri dari orang lain. Mereka biasa berbaur dengan atau pergi bersama dengan orang lain untuk menjaga kehidupan yang harmonis dan
    nyaman.
    <br/>
    <br/>
    <strong>Tipe 6 dan 7</strong>
    <br/>
    <br/>
    Apa yang sama: Skeptis Setia (6) dan Epicures (7) adalah jenis pusat kepala dan sayap masing-masing. Keduanya dapat saling berhubungan ide-ide yang beragam
    dan cepat mental, sering cerdas, analitis dan imajinatif.
    <br/>
    <br/>
    Apa yang berbeda: Skeptis Loyal sering menaruh spin negatif pada pengalaman, melihat kemungkinan terburuk dan perangkap, sementara Epicures menaruh spin
    positif pada pengalaman mereka, berencana untuk beberapa kemungkinan yang menyenangkan. Skeptis setia menyambut batas meyakinkan dan berusaha untuk
    mendapatkan kepastian. Epicures membenci batas dan berusaha untuk memperluas pilihan mereka. Kesenangan dan keinginan pribadi kekhawatiran sekunder untuk
    Skeptis Loyal, tetapi untuk Epicures mereka primer.
    <br/>
    <br/>
    <strong>Tipe 6 dan 8</strong>
    <br/>
    <br/>
    Apa yang sama: Skeptis Loyal Counter-fobia (6) dan pelindung (8) dianggap jenis mirip karena keduanya dapat menjadi agresif, menantang dan konfrontatif.
    Mereka bisa kenal takut dan akan berjuang untuk penyebab. Mereka berbagi pandangan dunia sebagai tidak ramah dan tidak dapat dipercaya.
    <br/>
    <br/>
    Apa yang berbeda: Perbedaan muncul dalam bagaimana dua jenis mengambil tindakan. Skeptis setia biasanya akan memiliki saat-saat ketakutan atau ragu-ragu
    sebelum mengambil tindakan - pembesar bahaya dan kadang-kadang memberikan cara di bawah tekanan karena keraguan dan pertanyaan muncul. Sebaliknya,
    pelindung akan bereaksi dari naluri - mengambil tindakan tanpa ragu-ragu, meminimalkan atau menolak bahaya, dan memegang tanah mereka sementara menyangkal
    kerentanan mereka.
    <br/>
    <br/>
    <strong>Tipe 6 dan 9</strong>
    <br/>
    <br/>
    Apa yang serupa: The Loyal Skeptic (6) adalah Mediator ini (9) titik stres dan Mediator adalah titik keamanan Loyal Skeptis ini. Kedua mediator dan lebih
    fobia Loyal Skeptis bisa menyenangkan, akomodatif, ramah, ingin menyenangkan, menonjolkan diri, sensitif dan ingin menghindari konflik. Skeptis setia dalam
    keamanan yang lebih nyaman, santai dan menerima hidup seperti itu. Mediator stres menjadi takut, mempertanyakan dan waspada.
    <br/>
    <br/>
    Apa yang berbeda: Skeptis Loyal menjaga jarak pribadi, berorientasi terhadap bahaya dan apa yang bisa salah, sementara mediator, yang paling
    "lain-referensi" jenis, sering kehilangan diri dalam permintaan dan mengklaim bahwa orang lain membuat atas mereka. Loyal tes Skeptis dan pertanyaan
    sebelum pergi bersama dengan orang lain, sedangkan mediator pergi bersama dengan orang lain sebelum pengujian dan pertanyaan.
    <br/>
    <br/>
    <strong>Jenis 7 dan 8</strong>
    <br/>
    <br/>
    Apa yang sama: Epicures (7) dan pelindung (8) adalah sayap masing-masing. Keduanya diri tegas, mengungkapkan keinginan dan keinginan mereka, percaya pada
    kekuatan dan kemampuan mereka sendiri, menolak batas dan kontrol, dan kesenangan-oriented. Mereka memiliki energi yang tinggi dan sedikit kekuatan menahan
    batin.
    <br/>
    <br/>
    Apa yang berbeda: Epicures menghindari rasa sakit, menjelaskan diri atau merasionalisasi kesulitan, melarikan diri konflik dan masuk ke perencanaan masa
    depan. Sebaliknya, pelindung menerima rasa sakit, terlibat dalam kesulitan, menghadapi konflik secara langsung dan hidup sebagian besar di masa sekarang.
    <br/>
    <br/>
    <strong>Jenis 7 dan 9</strong>
    <br/>
    <br/>
    Apa yang sama: Epicures (7) dan mediator (9) dapat mirip jenis karena mereka ingin hidup menjadi menyenangkan dan optimis. Kedua konflik-avoiders, mereka
    ingin disukai dan bergaul.
    <br/>
    <br/>
    Apa yang berbeda: Epicures lebih ingar-bingar dan cepat, sementara mediator lebih bahkan-marah dan lambat mondar-mandir. Epicures berorientasi terhadap
    diri mereka sendiri, mengetahui dan mengungkapkan keinginan mereka sendiri, agenda dan opini. Sebaliknya, mediator mengarahkan terhadap orang lain,
    melupakan atau menunda keinginan mereka sendiri, agenda dan opini.
    <br/>
    <br/>
    <strong>Jenis 8 dan 9</strong>
    <br/>
    <br/>
    Apa yang sama: Pelindung (8) dan mediator (9) adalah jenis pusat tubuh dan sayap masing-masing. Mereka menikmati kesenangan duniawi, merespon dengan reaksi
    usus, mencari kenyamanan, dan ramah dan sabar. Kedua mendapatkan dialihkan dari prioritas penting.
    <br/>
    <br/>
    Apa yang berbeda: Pelindung menyambut konflik dan bahkan marah, sedangkan mediator menghindari. Pelindung yang menentukan, dan akan mengungkapkan dan
    mempertahankan pendapat mereka sendiri sebagai fakta, sedangkan mediator mengarahkan pendapat orang lain dan pandangan, kehilangan posisi mereka sendiri
    untuk menghormati orang lain. Mereka sering tidak tegas dan "pergi bersama dengan orang lain untuk bergaul."<strong></strong>
